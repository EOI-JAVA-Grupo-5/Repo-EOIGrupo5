create table hilos
(
    id_creador     integer
        constraint fkp8840cp8foho3btiqdd8p0uf1
            references usuarios,
    fecha_creacion timestamp(6),
    id             bigint generated by default as identity
        primary key,
    mensaje_count  bigint,
    visitas        bigint,
    votos          bigint,
    descripcion    varchar(255),
    titulo         varchar(255)
);

alter table hilos
